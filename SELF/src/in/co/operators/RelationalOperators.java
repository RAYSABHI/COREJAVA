package in.co.operators;

public class RelationalOperators {
	
	public static void main(String[] args) {
		
		int a = 40;
		int b = 50;
		
		System.out.println("true/false " + (a<b) + " : a is greater");
		
		System.out.println("true/false: " + (a>b));
		
		System.out.println("true/false: " + (a<=b));
		
		System.out.println("true/false: " + (a>=b));
		
		System.out.println("true/false: " + (a==b));
		
		System.out.println("true/false: " + (a!=b));

	}

}
