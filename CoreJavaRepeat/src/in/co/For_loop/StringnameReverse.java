package in.co.For_loop;

public class StringnameReverse {
	public static void main(String[] args) {
	
		 // string name reverse
		String name = "Vijay Dinanath Chouhan";
		System.out.println(name); // simply print the name
		
		for (int i = name.length()-1; i>=0;i-- ) {
			// System.out.print(i+ " ");
			System.out.print(name.charAt(i) ); // reversing the name
		
		
	
	
	}
		
	}	
	
	}


