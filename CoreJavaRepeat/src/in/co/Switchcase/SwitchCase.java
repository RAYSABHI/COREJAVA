package in.co.Switchcase;

public class SwitchCase {
	public static void main(String args[]) {
		
		String Day = "Holiday";
		
		switch (Day) {
		
		case "Sunday":
			System.out.println("week off day");
			break;
			
		case "Monday":
			System.out.println("First Day of The Week");
			break;
			
		case "Tuesday":
			System.out.println("Second day of the week");
			break;

			default :
			System.out.println("Diwali ki chhutti");
			break;
			

		
		
	}
	}
	}
